        max_tag = 0
        if len( objects ) > 0:
            max_tag = max( [ len( owt.tag ) for owt in objects ] )
        blanks  = ' ' * max_tag
        for owt in objects:
            package = owt.package
            if package != '':
                package += '.'
            result.append( "        '%s': %s%s%s()" % 
                           ( owt.tag, blanks[ : max_tag - len( owt.tag ) ], 
                             package, owt.object.__class__.__name__ ) )
        return ',\n'.join( result )
        
